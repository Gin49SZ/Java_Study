public class Demo05VarPerson{
	public static void main(String[] args){
		// 姓名
		String name = "张三";
		// 性别
		char gender = 'M';
		// 年龄
		int age = 20;
		// 身高
		double height = 175.5;
		// 体重
		double weight = 145.5;
		
		System.out.println(name);
		System.out.println(gender);
		System.out.println(age);
		System.out.println(height);
		System.out.println(weight);
		
	}
}